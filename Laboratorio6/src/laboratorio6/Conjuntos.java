    private void btnDesarrolladorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDesarrolladorActionPerformed
        // TODO add your handling code here:
        btnConjunto.setEnabled(false);
        String persona = txtNombre.getText();
        
        
        if(chkWeb.isSelected() && chkJava.isSelected() && chkCelulares.isSelected()){
            Web.add(persona);
            Java.add(persona);
            Celulares.add(persona);
            lblRes5.setText(Web.size()+Java.size()+Celulares.size()+"");
            lblRes6.setText(Java.size()+"");
            interseccion = interseccion + 1;
            lblRes7.setText(interseccion+"");
        }
        else if(chkJava.isSelected()){
            Java.add(persona);
            lblRes6.setText(Java.size()+"");
        }
        else if(chkWeb.isSelected() && chkCelulares.isSelected()){
            Web.add(persona);
            Celulares.add(persona);
            lblRes7.setText(interseccion+"");
            interseccion = interseccion + 1;
        }
        if(chkWeb.isSelected() || chkCelulares.isSelected()){
            if(!(chkWeb.isSelected() && chkCelulares.isSelected())){
                if(chkWeb.isSelected()){
                    Web.add(persona);
                    if(Celulares.contains(persona))
                        interseccion = interseccion + 1;
                }
                else if(chkCelulares.isSelected()){
                    Celulares.add(persona);
                    if(Web.contains(persona))
                        interseccion = interseccion + 1;
                }
            }
            lblRes7.setText(interseccion+"");
            lblRes8.setText((Web.size()+Celulares.size()-interseccion)+"");
        }
        
        lblAtencion.setText(Web.size()+"");
        txtNombre.setText("");
        chkWeb.setSelected(false);
        chkJava.setSelected(false);
        chkCelulares.setSelected(false);
        btnMayor.setEnabled(true);

        
    }//GEN-LAST:event_btnDesarrolladorActionPerformed

    private void btnConjuntoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConjuntoActionPerformed
        // TODO add your handling code here:
        btnConjunto.setEnabled(false);
        rdbHashSet.setEnabled(false);
        rdbTreeSet.setEnabled(false);
        rdbLinkedHashSet.setEnabled(false);
        if(rdbHashSet.isSelected()){
            Web = factory.getSet("HS");
            Java = factory.getSet("HS");
            Celulares = factory.getSet("HS");
        }
        else if(rdbTreeSet.isSelected()){
            Web = factory.getSet("TS");
            Java = factory.getSet("TS");
            Celulares = factory.getSet("TS");
        }
        else if(rdbLinkedHashSet.isSelected()){
            Web = factory.getSet("LS");
            Java = factory.getSet("LS");
            Celulares = factory.getSet("LS");
        }
        
        txtNombre.setEnabled(true);
        chkWeb.setEnabled(true);
        chkJava.setEnabled(true);
        chkCelulares.setEnabled(true);
        
    }//GEN-LAST:event_btnConjuntoActionPerformed

    private void btnMayorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMayorActionPerformed
        // TODO add your handling code here:
        if(Web.size()>Celulares.size()){
            if(Web.size()>Java.size())
                JOptionPane.showMessageDialog(null, "Conjunto Web:\n"+Web.toString());
        }
        if(Celulares.size()>Web.size()){
            if(Celulares.size()>Java.size()){
                JOptionPane.showMessageDialog(null, "Conjunto Celulares:\n"+Celulares.toString());
            }
        }
        if(Java.size()>Web.size()){
            if(Java.size()>Celulares.size()){
                JOptionPane.showMessageDialog(null, "Conjunto Java:\n"+Java.toString());
            }
        }
    }//GEN-LAST:event_btnMayorActionPerformed

